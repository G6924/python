# Access Items

arr = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(f"ARR : {arr}")

print(arr[1])
print(arr[-1])

# Range of Indexes

print(arr[2:5])
print(arr[:5])
print(arr[2:])
print(arr[-4:-1])


# Check if Item Exists
if "apple" in arr :
    print("Yes, 'apple' is in  the fruits list")